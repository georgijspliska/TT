    public function approve($id)
    {
      $user = User::find($id);
      if($user->role == 3){
      User::where('id', '=', $id)->update(['role'=>1]);
      return redirect()->action('UserController@index', array('users' => User::all()->sortBy('name')));
      }
      else{
      User::where('id', '=', $id)->update(['role'=>3]);
      return redirect()->action('UserController@index', array('users' => User::all()->sortBy('name')));
      }
    }


